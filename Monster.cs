public class Monster
{
    public int ID;
    public string Name;
    public int MaxHitPoints;
    public int MaxDamage;

    public Monster(int id, string name, int maxDamage, int maxHitPoints)
    {
        ID = id;
        Name = name;
        MaxHitPoints = maxHitPoints;
        MaxDamage = maxDamage;
        
    }
}
